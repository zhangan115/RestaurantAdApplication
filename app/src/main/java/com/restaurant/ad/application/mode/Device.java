package com.restaurant.ad.application.mode;

public class Device {
    private String padNum;
    private long padId;
    private long numberUnits;

    public String getPadNum() {
        return padNum;
    }

    public void setPadNum(String padNum) {
        this.padNum = padNum;
    }

    public long getPadId() {
        return padId;
    }

    public void setPadId(long padId) {
        this.padId = padId;
    }

    public long getNumberUnits() {
        return numberUnits;
    }

    public void setNumberUnits(long numberUnits) {
        this.numberUnits = numberUnits;
    }
}
